#include <stdio.h>

int main()
{
int a = 5;
int b = 6;

int add = a + b;
printf("%i \n",add); // Output --> 11

int sub = a - b;
printf("%i\n",sub); // Output --> -1

int mul = a * b;
printf("%i\n",mul); // Output --> 30

int div1 = a / b;
printf("%i\n",div1); // Output --> 0

int mod = a % b;
printf("%i\n",mod); // Output --> 5


return 0;


}


