#include <stdio.h>

int main()
{
    int a = 10; 
    
    a +=5; // a = a+5
    printf("%i\n",a); // 15
    
    a -=5; // a = a-5
    printf("%i\n",a); // 10
    
    a *=5; // a = a*5
    printf("%i\n",a); // 50
    
    a /=2; // a = a/2
    printf("%i\n",a); // 25
    
    a %= 7; // a = a % 7
    printf("%i\n",a); // 4


}


